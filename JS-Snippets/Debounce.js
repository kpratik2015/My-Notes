/**
It is used to invoke/call/execute function only when things have stopped happening for a given specific time.
*/

function debounce(func, timeout = 300){
  let timer;
  return (...args) => {
    clearTimeout(timer);
    timer = setTimeout(() => { func.apply(this, args); }, timeout);
  };
}
